var ghost = 'Pinky';
var colour;

switch (ghost) {
  case 'Inky':
    colour = 'Cyan';
    break;
  case 'Blinky':
    colour = 'Red';
    break;
  case 'Pinky':
    colour = 'pink';
    break;
  case 'Clyde':
    colour = 'orange';
    break;
  default:

}

console.log(ghost + ' is the colour ' + colour);
